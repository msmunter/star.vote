	<h3>Notes on additional voter identity documentation:</h3>
	<p>
		To prove that you are eligible to vote, you must upload a photo of a document verifying your name and address.
	</p>
	<p style="font-weight: bold;">
		The ballot you receive from your county election administrators is the document we recommend you use for identity verification.<br />
		Other acceptable documents include a utility bill, bank statement, government document, photo ID, or voter registration card.
	</p>
	<p>
		If you have any problems voting you may contact our voter support team by emailing us at <a href="mailto:support@equal.vote">support@equal.vote</a>.
	</p>
